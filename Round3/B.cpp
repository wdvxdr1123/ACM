using namespace std;

const int maxn =1e2+7;

int n,m,p[maxn],h[maxn],c[maxn];

int main()
{
    int t;
    scanf("%d",&t);
    while(t--)
    {
        scanf("%d%d",&n,&m);
        for(int i=1;i<=m;i++)
            scanf("%d%d%d",&p[i],&h[i],&c[i]);
        
    }
    return 0;
}